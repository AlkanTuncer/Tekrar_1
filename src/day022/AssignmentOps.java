package day022;

public class AssignmentOps {
    public static void main(String[] args) {
        // +, -, *, /, %
        // +=, -=, *=, /=, %=

        int k=3;
        k+=10; // k=k+10;  k 13
        k-=5;  // k=k-5;   k 8
        k*=2;  // k=k*2;   k 16
        k/=4;  // k=k/4;   k 4
        k%=3;  // k=k%3;   k 1
        System.out.println(k);

        // Tanımlanan tam sayı bir değişkenin değerini 3 artıran Java kodunu yazınız.
        int r=50;
        r+=3;
        // r=r+3;
        System.out.println(r);

    }
}
